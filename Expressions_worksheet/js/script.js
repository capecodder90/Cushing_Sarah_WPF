// Problem: Dog Years
var humanYrs = 12; // Sparky's age in human years
var humanToDog = 7; // 1 human year is 7 dog years
var dogYrs = "Sparky is " + humanYrs + " human years old which is " + humanYrs * humanToDog + " in dog years."; // result container, calculates humanYrs, which is 12 years, times humanToDog, which is 7 years, and concatenates the strings in the expression.

console.log(dogYrs); // displays the result of the calculations and concatenation of the variable dogYrs in the console of the wbe browser

